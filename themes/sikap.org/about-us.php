                            <strong>Quality Program Diversification</strong>
                            <p>SIKAP should expand and diversify into integrated/holistic, environment-oriented, gender-sensitive, innovative, responsive, sustainable and high impact programs and projects.</p>
                        </li>
                        <li>
                            <strong>Strengthening Linkages and Partnerships</strong>
                            <p>SIKAP shall strengthen existing partnerships, alliances and linkages, and expand network building in technical resources, educational and civic institutions, national and international funding agencies, other non-government and government agencies. This will ensure that human, financial and other relevant expertise and knowledge in these institutions will be put to good use in SIKAP’s development work.</p>
                        </li>
                        <li>
                            <strong>Pursue Quality Systems and Procedures</strong>
                            <p>SIKAP shall develop quality systems to become effective and efficient. These shall include administrative systems and policies, human resource management systems and policies, management information systems, management planning and communication systems, and programs/projects performance information, monitoring and evaluation systems, all of which must be enhanced and improved.</p>
                        </li>
                        <li>
                            <strong>Enhancement of Employee’s Professional and Technical capabilities</strong>
